<template>
  <div class="row row-bordered no-gutters">
    <div v-if="$slots.header || header" class="col-12 col-md-4">
      <slot name="header">
        <span class="weight-semibold">
          {{ header }}
        </span>
      </slot>
    </div>
    <div class="col-12" :class="[{ 'col-md-6': $slots.button }, { 'col-md-8': !$slots.button }]">
      <slot />
    </div>
    <div v-if="$slots.button" class="col-12 col-md-2 align-text-md-right">
      <slot name="button" />
    </div>
  </div>
</template>

<script setup lang="ts">
import { provide } from 'vue'
import { formId } from 'dkfds-vue3-utils'

const { header = null, id = null } = defineProps<{
  header?: string | null
  id?: string | null
}>()
/**
 * Form id der bruges i slots
 * eg. label for input element
 */
const { formid } = formId(id, true)
/**
 * Provide for underliggende elementer
 * eg. label for input element
 */
provide('formid', formid)
</script>

